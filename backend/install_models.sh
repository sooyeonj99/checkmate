#!/bin/bash
# ──────────────────────────────────────────────────────────────────────────────
# Checkmate 백엔드 AI 모델 설치 스크립트
# 실행: bash install_models.sh
# ──────────────────────────────────────────────────────────────────────────────

set -e  # 오류 발생 시 즉시 중단

echo "══════════════════════════════════════════════"
echo "  Checkmate 개인정보 마스킹 모델 설치"
echo "══════════════════════════════════════════════"

# ── 1. 의존 패키지 설치 ──────────────────────────────────────────────────────
echo ""
echo "[1/3] Presidio + spaCy 패키지 설치 중..."
pip install \
    "presidio-analyzer==2.2.354" \
    "presidio-anonymizer==2.2.354" \
    "spacy==3.7.4"
echo "✅ 패키지 설치 완료"

# ── 2. 영어 spaCy 모델 설치 (필수) ──────────────────────────────────────────
echo ""
echo "[2/3] 영어 spaCy 모델 설치 중 (en_core_web_sm, 필수)..."
python -m spacy download en_core_web_sm
echo "✅ 영어 모델 설치 완료"

# ── 3. 한국어 spaCy 모델 설치 (선택, NER 성능 향상) ─────────────────────────
echo ""
echo "[3/3] 한국어 spaCy 모델 설치 중 (ko_core_news_sm, 선택)..."
echo "      ※ 이름/기관명/장소 AI 감지 성능이 향상됩니다."
if python -m spacy download ko_core_news_sm; then
    echo "✅ 한국어 모델 설치 완료"
else
    echo "⚠️  한국어 모델 설치 실패 (영어 모델 + 정규식으로 대체됩니다)"
fi

# ── 완료 ─────────────────────────────────────────────────────────────────────
echo ""
echo "══════════════════════════════════════════════"
echo "  설치 완료! 백엔드를 재시작해 주세요."
echo "  python -m uvicorn app.main:app --reload"
echo "══════════════════════════════════════════════"
